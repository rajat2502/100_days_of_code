// privacy for a property
// common convention is to place an underscore _ before the name of a property to mean that the property should not be altered
// although they can still be reassigned as
// they should not be accessed directly
const robot = {
  _energyLevel: 100,
  recharge(){
    this._energyLevel += 30;
    console.log(`Recharged! Energy is currently at ${this._energyLevel}%.`)
  }
};

robot._energyLevel = 'high';
robot.recharge();

// getters methods
// they need not to be called using parenthesis
const robot = {
  _model: '1E78V2',
  _energyLevel: 100,
  get energyLevel(){
    if((typeof this._energyLevel) == 'number'){
      return `My current energy level is ${this._energyLevel}`;
    }
    else{
      return 'System malfunction: cannot retrieve energy level'
    }
  }
};

// call get 
console.log(robot.energyLevel);

// setters methods
const robot = {
  _model: '1E78V2',
  _energyLevel: 100,
  _numOfSensors: 15,
  get numOfSensors(){
    if(typeof this._numOfSensors === 'number'){
      return this._numOfSensors;
    } else {
      return 'Sensors are currently down.'
    }
  },
  set numOfSensors(num){
    if((typeof num) === 'number' && num >= 0){
      this._numOfSensors = num;
    }
    else{
      console.log('Pass in a number that is greater than or equal to 0');
    }
  }
};

robot.numOfSensors = 100;
console.log(robot.numOfSensors);


